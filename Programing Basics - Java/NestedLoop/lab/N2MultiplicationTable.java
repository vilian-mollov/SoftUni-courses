package NestedLoop.lab;

public class N2MultiplicationTable {
    public static void main(String[] args) {


        for (int i = 1; i <= 10; i++) {

            for (int j = 1; j <= 10; j++) {
                int h = i * j;
                System.out.println(i  + " * " + j  + " = " + h );
            }


        }



    }
}
